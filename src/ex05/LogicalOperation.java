package ex05;

public class LogicalOperation {
    public static void main(String[] args) {
        int x = 3;
        int y = 4;
        System.out.println((x == 3) && (y == 7));
        System.out.println((x == 3) || (y == 5));

        // 논리연산자
        // && 그리고(and)
        // || 거나 (or)
    }

}
